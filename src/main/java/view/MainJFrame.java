    public void loadSaveAgents(){
        this.delAllList();
        for(Agente agente : DataSaveUtilies.cargarAgentes()){
            String dataAgente = agente.getID() + "|" + agente.getNome() + "|" + agente.getSaldo();
            this.addAgent(dataAgente);
        }
    }
    
